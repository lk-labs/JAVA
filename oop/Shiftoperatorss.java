package oop;

public class Shiftoperatorss {
    public static void main(String[] args) {
        System.out.println(13>>2);
        System.out.println(13>>>2);
        System.out.println(13<<2);
        System.out.println((0)^(1));
        System.out.println((3>2)^(2>7));
        System.out.println((3>2)|(2>7));

    }
}

public class Leftshiftoperators{
	public static void main(String []args){
		int x=10;
		System.out.println(x<<2);//10*2^2=40
		System.out.println(x<<3);//10*2^3=80
		System.out.println(x>>2);//10/2^2=2
		System.out.println(x>>3);//10/2^3=1
		System.out.println(-20<<2);//outputs -80
		System.out.println(-20>>2);//outputs -5
	}
}
